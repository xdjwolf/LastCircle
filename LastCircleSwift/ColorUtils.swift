    class func randomColor(alpha: CGFloat = 1) -> UIColor {
        if GameUtils.sharedUtils.mode == .BlackWhite {
            return UIColor(white: 1, alpha: alpha)
        }
        let index = Int(arc4random_uniform(100)) % colors.count
        let rgb = colors[index]
        let r = CGFloat(rgb[0])/255
        let g = CGFloat(rgb[1])/255
        let b = CGFloat(rgb[2])/255
        
        return UIColor(red:r, green:g, blue:b, alpha:alpha)
    }
    
